def make_course_from_observation(obs: dict[str, Any], detail: dict[str, Any] | None = None) -> dict[str, Any]:
    detail = detail or {}
    text = detail.get("text", "")
    title = detail.get("title") or obs.get("title", "")
    source_url = obs.get("source_url") or detail.get("source_url")
    org = detail.get("organizer") or obs.get("organizer") or ""
    views = obs.get("views")
    likes = obs.get("likes")
    hot_notes = []
    if views is not None:
        hot_notes.append(f"Accupass瀏覽 {views}")
    if likes is not None:
        hot_notes.append(f"收藏/喜歡 {likes}")
    for token in ["額滿", "完售", "熱賣", "加開", "好評", "席位有限"]:
        if token in f"{title} {text}":
            hot_notes.append(token)
    course_id = hashlib.md5(f"{source_url}|{title}".encode("utf-8")).hexdigest()[:10]
    return {
        "id": course_id,
        "title": title,
        "category": classify_category(title, text[:1200]),
        "provider": org or infer_provider_from_url(source_url),
        "source_url": source_url,
        "format": "線下/實體" if "Offline Event" in text or "線下" in text or "實體" in text else "需確認",
        "date": detail.get("date") or obs.get("time_text", "未公開"),
        "location": detail.get("location") or obs.get("location", "未公開"),
        "price": extract_price(text) if text else "未公開/需洽詢",
        "price_type": "公開標價" if text and extract_price(text) != "未公開/需洽詢" else "未公開/需洽詢",
        "teacher": extract_teacher(text) if text else "未公開/機構講師群",
        "teacher_background": summarize_teacher_background(text),
        "target_company": infer_target_company(title, text),
        "hotness": "；".join(hot_notes) if hot_notes else "未見公開量化熱度",
        "reputation": infer_reputation(title, text, views, likes),
        "evidence_level": detail.get("evidence_level", obs.get("type", "source_page")),
        "research_note": make_research_note(title, text),
    }


