def summarize_teacher_background(text: str) -> str:
    if not text:
        return "未公開"
    cues = ["現任", "曾任", "前", "經驗", "顧問", "教授", "董事長", "總經理", "執行長", "創辦人"]
    sentences = re.split(r"[。！？\n]", text)
    picked: list[str] = []
    for sent in sentences:
        sent = re.sub(r"\s+", " ", sent).strip()
        if 20 <= len(sent) <= 160 and any(cue in sent for cue in cues):
            picked.append(sent)
        if len(picked) >= 2:
            break
    return "；".join(picked) if picked else "未公開/請見來源頁"


